const sayColor = (color) => {
  switch (color) {
    case "orange":
      return "light color";
    case "pink":
      return "light color";
    case "yellow":
      return "light color";
    case "blue":
      return "dark color";
    case "purple":
      return "dark color";
    case "brown":
      return "dark color";

    default:
      return "unKnown color";
  }
};

